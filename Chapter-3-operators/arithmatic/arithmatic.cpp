#include <iostream>
using namespace std;

int main() {
    int a , b;
    a = 10;
    b = 20;
    // addition
    cout << "Addition of a and b is " << a + b << endl;
    // subtraction
    cout << "Subtraction of a and b is " << a - b << endl;
    // multiplication
    cout << "Multiplication of a and b is " << a * b << endl;
    // division
    cout << "Division of a and b is " << a / b << endl;
    // modulus
    cout << "Modulus of a and b is " << a % b << endl;
    int age = 20;
    //increment operator
    age++;
    cout << "Age after increment is " << age << endl;
    //decrement operator
    age--;
    age--;
    cout << "Age after decrement is " << age << endl;
    return 0;
}